func TestMime(t *testing.T) {
	type oneresult struct {
		path       []int
		mediaType string
	}
	type onetest struct {
		msg     string
		results []oneresult
	}
	tests := []onetest{
		{msg1, []oneresult{
			{[]int{}, "multipart/mixed"},
			{[]int{0}, "text/plain"},
			{[]int{1}, "application/octet-stream"},
		}},
		onetest{msg2, []oneresult{
			{[]int{}, "multipart/mixed"},
			{[]int{0}, "text/plain"},
			{[]int{1}, "multipart/alternative"},
			{[]int{1, 0}, "multipart/related"},
			{[]int{1, 0, 0}, "text/plain"},
			{[]int{1, 0, 1}, "text/plain"},
			{[]int{1, 1}, "multipart/related"},
			{[]int{1, 1, 0}, "application/pdf"},
			{[]int{1, 1, 1}, "application/pdf"},
		}},
	}
	for _, onet := range tests {
		testwalk := func(pc *ParserContext, path []int, r io.Reader, pd PartDescr) error {
			i := pc.Ctx.(int)
			if false {
				fmt.Printf("%v %v\n", path, pd.MediaType)
			}
			if i >= len(onet.results) {
				return nil
			}
			if !reflect.DeepEqual(onet.results[i].path, path) {
				t.Error(fmt.Sprintf("%v %v\n", onet.results[i].path, path))
			}
			if !reflect.DeepEqual(onet.results[i].mediaType, pd.MediaType) {
				t.Error(fmt.Sprintf("%v %v\n", onet.results[i].mediaType, pd.MediaType))
			}
			pc.Ctx = i + 1
			return nil
		}
		pc := &ParserContext{}
		pc.Ctx = 0
		err := WalkParts(strings.NewReader(onet.msg), testwalk, pc, 10)
		if err != nil {
			t.Error(err.Error())
		}
	}
}

func printM(depth int, m *MimePart) {
	fmt.Printf("%s%s %v\n", strings.Repeat(" ", depth), MimeTypeTxt(m.MimeType), m)
	if m.Child != nil {
		printM(depth+1, m.Child)
	}
	for cur := m.Next; cur != nil; cur = cur.Next {
		printM(depth, cur)
	}
}

func TestMimeTree(t *testing.T) {
	tree, err := GetMimeTree(strings.NewReader(msg2), 10)
	if err != nil {
		t.Error(err.Error())
	}
	//for debugging
	if false {
		printM(0, tree)
	}
	cur := tree
	check := func(m *MimePart, mi MimeTypeInt, NextSet bool, prevSet bool, ChildSet bool, ParentSet bool) bool {
		if m.MimeType.MimeTypeInt != mi {
			t.Error(fmt.Sprintf("Unexpected type %v: %v", MimeTypeTxt(m.MimeType), m))
		}
		if cur.Next == nil {
			if NextSet {
				t.Error(fmt.Sprintf("Next should be set: %v", m))
			}
		} else {
			if !NextSet {
				t.Error(fmt.Sprintf("Next should not be set: %v", m))
			}
		}
		if cur.Prev == nil {
			if prevSet {
				t.Error(fmt.Sprintf("prev should be set: %v", m))
			}
		} else {
			if !prevSet {
				t.Error(fmt.Sprintf("prev should not be set: %v", m))
			}
		}
		if cur.Child == nil {
			if ChildSet {
				t.Error(fmt.Sprintf("Child should be set: %v", m))
			}
		} else {
			if !ChildSet {
				t.Error(fmt.Sprintf("Child should not be set: %v", m))
			}
		}
		if cur.Parent == nil {
			if ParentSet {
				t.Error(fmt.Sprintf("Parent should be set: %v", m))
			}
		} else {
			if !ParentSet {
				t.Error(fmt.Sprintf("Parent should not be set: %v", m))
			}
		}
		return true
	}
	check(cur, MultipartMixed, false, false, true, false)
	cur = cur.Child
	check(cur, TextPlain, true, false, false, true)
	cur = cur.Next
	check(cur, MultipartAlternative, false, true, true, true)
	cur = cur.Child
	related := cur
	check(cur, MultipartRelated, true, false, true, true)
	cur = cur.Child
	check(cur, TextPlain, true, false, false, true)
	cur = cur.Next
	check(cur, TextPlain, false, true, false, true)
	cur = cur.Parent
	check(cur, MultipartRelated, true, false, true, true)
	if cur != related {
		t.Error("Broken tree structure")
	}
	cur = cur.Next
	intermediary := cur
	check(cur, MultipartRelated, false, true, true, false)
	cur = cur.Child
	check(cur, MimeTypeOther, true, false, false, true)
	cur = cur.Next
	check(cur, MimeTypeOther, false, true, false, true)
	if intermediary != cur.Parent {
		t.Error("Broken tree structure 2")
	}
}
